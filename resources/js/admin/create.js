var $ = require( "jquery" );

$(document).ready(function() {


  $('input[name="price"]').keyup( function() {
    let priceInputValue = $('input[name="price"]').val()
      if (priceInputValue.includes('.')) {
        $('.edit-price-error').addClass('visible');
      } else {
        $('.edit-price-error').removeClass('visible');
      }
  });

}); // End document ready
